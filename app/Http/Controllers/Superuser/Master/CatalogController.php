    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        if(Auth::user()->is_superuser == 0){
            if(empty($this->access)){
                return redirect()->route('superuser.index')->with('error','Anda tidak punya akses untuk membuka menu terkait');
            }
        }

        if ($request->ajax()) {
            $validator = Validator::make($request->all(), [
                'code' => 'required|string',
                'brand' => 'required|string',
                'note' => 'nullable|string',
            ]);

            if ($validator->fails()) {
                $response['notification'] = [
                    'alert' => 'block',
                    'type' => 'alert-danger',
                    'header' => 'Error',
                    'content' => $validator->errors()->all(),
                ];
  
                return $this->response(400, $response);
            }

            if ($validator->passes()) {
                $catalog = new Catalog;

                $catalog->code = $request->code;
                $catalog->brand = $request->brand;
                $catalog->note = $request->note;
                $catalog->print_count = 0;
                $catalog->created_by = Auth::id();

                $catalog->status = Catalog::STATUS['ACTIVE'];

                if ($catalog->save()) {
                    $response['notification'] = [
                        'alert' => 'notify',
                        'type' => 'success',
                        'content' => 'Success',
                    ];

                    $response['redirect_to'] = route('superuser.master.catalog.index');

                    return $this->response(200, $response);
                }
            }
        }
    }

